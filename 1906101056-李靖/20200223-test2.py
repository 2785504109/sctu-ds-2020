'''
求数组中的前n个数的平方和：[14,25,98,75,23,1,4,56,59]
要求：n需要是input输入，且小于数组长度，不能固定。
'''
n=int(input('请输入一个整数：'))
l=[14,25,98,75,23,14,56,59]
sum=0
if n>len(l):
	print('超出数组长度')
else:
	for i in l[0:n+1]:
		sum=sum+i*i
print(sum)

