
#求给定数的阶乘,所求阶乘的数不可以是这几个数：[1,10,20,30,40,50]

n=int(input('请输入一个正整数：'))
m=1
if n==1 or n==10 or n==20 or n==30 or n==40 or n==50:
	print('%d不能求阶乘' %(n))
else:
	for i in range(1,n+1):
		m=m*i
	print('%d的阶乘为%d' %(n,m))


